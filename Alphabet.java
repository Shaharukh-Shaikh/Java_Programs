class Alphabet 
{
	public static void main(String[] args) 
	{
		char a='A';
		if(a>='a' && a<='z')
		{
			System.out.println(a+"   Is An Alphabet");		
		}
		else if(a>='A' && a<='Z')
		{
			System.out.println(a+"   Is An Alphabet");
		}
		else
		{
			System.out.println(a+"   Is Not And Alphabet");
		}
	}
}
